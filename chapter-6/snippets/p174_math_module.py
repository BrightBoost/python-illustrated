import math

x = math.sqrt(25)
print("The square root of 25 is", x)

# Raise something to the power of something else with math.pow(base, exponent)
y = math.pow(2, 3)
print("2 to the power of 3 is", y) # 8.0

# Calculate factorial with math.factorial(x)
print(math.factorial(5)) # 120


angle_in_radians = math.radians(90) # convert 90 degrees to radians
print(math.sin(angle_in_radians)) # 1.0 (approximately)


math.log(x)


print(math.log(10)) # ~2.302585 (natural log of 10)
print(math.log10(10)) # 1.0 (log base 10 of 10)


print(math.ceil(3.2)) # 4, rounding up (the ceiling)
print(math.floor(3.8)) # 3, rounding down (the floor)


print("Pi is about", math.pi)
print("e is about", math.e)