# Getting all keys
cat_attributes = {
    "name": "Zia",
    "age": 1,
    "color": "grey",
}
keys = cat_attributes.keys()
print("Keys:", list(keys))

# Getting all values
cat_attributes = {
    "name": "Zia",
    "age": 1,
    "color": "grey",
}
values = cat_attributes.values()
print("Values:", list(values))

# Getting key-value pairs
cat_attributes = {
    "name": "Zia",
    "age": 1,
    "color": "grey",
}
items = cat_attributes.items()
print("Items:", list(items))